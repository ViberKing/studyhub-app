type NavItem = { section: string; page?: undefined; label?: undefined; icon?: undefined } | { page: string; label: string; icon: React.ReactNode; section?: undefined };

export default function Sidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const searchParams = useSearchParams();
  const isDemo = searchParams.get("demo") === "true";
  const demoSuffix = isDemo ? "?demo=true" : "";

  const currentPage = pathname.split("/").pop() || "dashboard";

  return (
    <aside className="sidebar">
      <div className="sidebar-brand">
        <div className="logo-mark">
          <svg width="16" height="16" fill="none" stroke="white" strokeWidth="2.5" strokeLinecap="round" strokeLinejoin="round" viewBox="0 0 24 24">
            <path d="M2 3h6a4 4 0 0 1 4 4v14a3 3 0 0 0-3-3H2z" />
            <path d="M22 3h-6a4 4 0 0 0-4 4v14a3 3 0 0 1 3-3h7z" />
          </svg>
        </div>
        <h2>Study-HQ</h2>
      </div>

      {(navItems as unknown as NavItem[]).map((item, i) => {
        if (item.section) {
          return <div key={i} className="sidebar-section">{item.section}</div>;
        }
        const active = currentPage === item.page;
        return (
          <div
            key={item.page}
            className={`nav-item${active ? " active" : ""}`}
            role="button"
            tabIndex={0}
            aria-current={active ? "page" : undefined}
            onClick={() => router.push(`/${item.page}${demoSuffix}`)}
            onKeyDown={(e) => {
              if (e.key === "Enter" || e.key === " ") {
                e.preventDefault();
                router.push(`/${item.page}${demoSuffix}`);
              }
            }}
          >
            <span className="ico">{item.icon}</span>
            <span>{item.label}</span>
          </div>
        );
      })}
    </aside>
  );
}
